package MethodsAndConstructors.Cwiczenie2;

public class PrzeliczanieJednostek {

    double metryNaCentymetry (double metry){
        return metry * 100;
    }

    double metryNaMilimetry(double metry){
        return metry * 1000;
    }

    double centymetryNaMetry (double cm){
        return cm / 100;
    }

    double milimetryNaMetryt (int mm){
        return mm / 1000;
    }


}
